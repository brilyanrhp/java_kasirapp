    class MyList implements ListSelectionListener{
        @Override
        public void valueChanged(ListSelectionEvent e) {
            if(e.getValueIsAdjusting()){
                return;
            }
            ListSelectionModel lsm = (ListSelectionModel) e.getSource();
            selectedRow = lsm.getMinSelectionIndex();
            TableModel tb = tbTransaksi.getModel();
        }
    }
    
    private void pilih() {
        tbTransaksi.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ListSelectionModel rowSel = tbTransaksi.getSelectionModel();
        rowSel.addListSelectionListener(new MyList());
    }

    private void viewData() {
        index = 0;
        DefaultTableModel model;
        model = (DefaultTableModel) tbTransaksi.getModel();
        
        Connect c = new Connect();
        
        Object[][] data = c.getDataTrans();
        if(model.getRowCount() == 0){
            Object[] row;
            for (int i = 0; i < c.getCountTrans(); i++){
                row = new Object[]{data[i][0], data[i][1], data[i][2], data[i][3], data[i][4], data[i][5]};
                model.addRow(row);
                index++;
            }
        }
    }
    public void getCombo(){
        try {
            Connect c = new Connect();
            Statement sta = c.con.createStatement();
            ResultSet rsa = sta.executeQuery("select * from barang");
            while(rsa.next()){
                String cbName = rsa.getString("nama_barang");
                cbNama.addItem(cbName);
            }
            
        } catch (Exception e) {
        }
    }
    
