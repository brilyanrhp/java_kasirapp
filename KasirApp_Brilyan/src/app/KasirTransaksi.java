    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        Connect c = new Connect();
        c.insertTrans(cbNama.getSelectedItem().toString(), tfBanyak.getText());
        if(c.cekError){
            JOptionPane.showMessageDialog(null, "Cek barang !!!");
        }else{
            c.setDataPgw();
            DefaultTableModel model = (DefaultTableModel) tbTransaksi.getModel();
            Object[] row = new Object[]{c.getIdTrans(), cbNama.getSelectedItem().toString(), c.getHarga(), tfBanyak.getText(), c.getTotal(), c.getNama()};
            model.addRow(row);
            int harga_value = c.getHargaTotal();
            String f_harga = String.format("%,d", harga_value);
            txtTotal.setText("Rp "+f_harga+",-");  
        }
        
    }//GEN-LAST:event_btnTambahActionPerformed

