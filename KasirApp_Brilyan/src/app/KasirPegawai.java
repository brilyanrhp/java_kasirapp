    class MyList implements ListSelectionListener{
        @Override
        public void valueChanged(ListSelectionEvent e) {
            if(e.getValueIsAdjusting()){
                return;
            }
            ListSelectionModel lsm = (ListSelectionModel) e.getSource();
            selectedRow = lsm.getMinSelectionIndex();
            TableModel tb = tbPgw.getModel();
            
            tfId.setText(tb.getValueAt(selectedRow, 0).toString());
            tfNama.setText(tb.getValueAt(selectedRow, 1).toString());
            tfStatus.setText(tb.getValueAt(selectedRow, 2).toString());
            tfId.setEditable(false);
            tfStatus.setEditable(false);
        }
    }
    
    private void pilih() {
        tbPgw.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ListSelectionModel rowSel = tbPgw.getSelectionModel();
        rowSel.addListSelectionListener(new MyList());
    }

    private void viewData() {
        index = 0;
        DefaultTableModel model;
        model = (DefaultTableModel) tbPgw.getModel();
        
        Connect c = new Connect();
        
        Object[][] data = c.getDataPgw();
        if(model.getRowCount() == 0){
            Object[] row;
            for (int i = 0; i < c.getCountPgw(); i++){
                row = new Object[]{data[i][0], data[i][1], data[i][2]};
                model.addRow(row);
                index++;
            }
        }
        
    }
