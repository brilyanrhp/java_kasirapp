    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel mod = (DefaultTableModel) tbPgw.getModel();
        String id = mod.getValueAt(selectedRow, 0).toString();
        
        for(int i = selectedRow; i < index-1; i++){
            mod.setValueAt(mod.getValueAt(i + 1, 0), i, 0);
            mod.setValueAt(mod.getValueAt(i + 1, 1), i, 1);
            mod.setValueAt(mod.getValueAt(i + 1, 2), i, 2);
        }
        
        mod.setValueAt("", index-1, 0);
        mod.setValueAt("", index-1, 1);
        mod.setValueAt("", index-1, 2);
        
        tfId.setText("");
        tfNama.setText("");
        tfStatus.setText("");
        
        Connect c = new Connect();
        c.delPgw(id);
        System.out.println("hapus sukses");
        
    }//GEN-LAST:event_btnHapusActionPerformed

